package tp04.ejercicio5;

public class AreaEmpresa {
	private String id;
	private Integer time;
	
	public AreaEmpresa(String id,Integer time) {
		this.id=id;
		this.time=time;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Integer getTime() {
		return time;
	}

	public void setTime(Integer time) {
		this.time = time;
	}
	
	
}
